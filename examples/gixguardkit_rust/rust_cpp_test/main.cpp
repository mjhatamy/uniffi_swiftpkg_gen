void print_named_lines(uint8_t *b, int total_len, const char *name) {
    printf("%s: ", name);
    for(int i=0; i < total_len; i++) {
        printf("%#02x", b[i]);
        if(i<GXT_KEY_LEN-1) printf(":");
    }
    printf("\n");
}

